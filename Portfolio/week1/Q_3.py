# 3. Over the summer, temperatures in Yorkshire reached 38.4C. Write a program that
# converts this value in Celsius to the equivalent temperature in Fahrenheit, and then displays both.


celsius_temp = 38.4

# converting Celsius to Fahrenheit
fahrenheit_temp = (9/5) * celsius_temp + 32

# displyaing results
print(f"Temperature in Celsius: {celsius_temp}C")
print(f"Temperature in Fahrenheit: {fahrenheit_temp}C")